/***********************************************************
* double am_exp(double x)
*
* Purpose:
*   A wrapper to avoid arguments to exp() that produce
*   denormal results.
*
*   The smallest normalized IEEE 754 double is
*
*     eps = 2^-1022 = 2.2250738585072014e-308
*
*     log(eps) = -1022 * log(2) = -708.3964185322641
*
*  and the smallest denormal double is
*
*    eps' = 2^-(1022 + 52) = 5e-324
*
*    log(eps') = -(1022 + 52) * log(2) = -744.4400719213812
*
*  The exp() implementations in both gcc and pgcc take about
*  an order of magnitude longer for log(eps') <~ x < log(eps)
*  than for x outside this range.  Interestingly, both take
*  about the same amount of time for x <~ log(eps') and for
*  x > log(eps), indicating that the zero results returned
*  for x <~ log(eps') are computed rather than based on a
*  simple test.  Presumably this is an optimization to
*  slightly improve speed for x > log(eps).  The choice of
*  whether to use this function or simply exp() in various
*  parts of the am code depends on whether or not a
*  significant fraction of arguments that will produce
*  denormal results is expected.
*
* Arguments:
*   double x
*
* Return:
*   exp(x), or zero if x < log(eps)
************************************************************/

double am_exp(double x)
{
    return x < -708.3964185322641 ? 0.0 : exp(x);
}   /* am_exp() */


