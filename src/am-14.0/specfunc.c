double am_K0(double x)
{
    unsigned int i, ip;
    double a, b, c, d;
    double u;

    if (x < 0.5) {
        u = -log(x);
        x *= K0_XSCALE;
        i = (unsigned int)(x);
        ip = i + 1;
        a = (double)ip - x;
        b = 1. - a;
        c = a * a * a - a;
        d = b * b * b - b;
        return u + a * K0_y[i] + b * K0_y[ip] + c * K0_y2[i] + d * K0_y2[ip];
    }
    u = K0_USCALE / x;
    i = (unsigned int)(u);
    ip = i + 1;
    a = (double)ip - u;
    b = 1. - a;
    c = a * a * a - a;
    d = b * b * b - b;
    return a * K0_v[i] + b * K0_v[ip] + c * K0_v2[i] + d * K0_v2[ip];
}   /* am_K0() */


