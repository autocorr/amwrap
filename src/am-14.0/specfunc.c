double am_xK1(double x)
{
    unsigned int i, ip;
    double a, b, c, d;
    double u;

    if (x < 0.5) {
        x *= XK1_XSCALE;
        i = (unsigned int)(x);
        ip = i + 1;
        a = (double)ip - x;
        b = 1. - a;
        c = a * a * a - a;
        d = b * b * b - b;
        return a * XK1_y[i] + b * XK1_y[ip] + c * XK1_y2[i] + d * XK1_y2[ip];
    }
    u = XK1_USCALE / x;
    i = (unsigned int)(u);
    ip = i + 1;
    a = (double)ip - u;
    b = 1. - a;
    c = a * a * a - a;
    d = b * b * b - b;
    return a * XK1_v[i] + b * XK1_v[ip] + c * XK1_v2[i] + d * XK1_v2[ip];
}   /* am_xK1() */


